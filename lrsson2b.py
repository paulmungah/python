# Tuple
# A tuple is an immutable type of a list (It cannot change)
# To introduce a tuple,we use the paranthesis ()

counties=("Nairobi," "Mombasa", "Nakuru", "Eldoret", "Kisumu")

print(counties)
print(type(counties))

# Slicing of tuples
print(counties[3:1])

# accessing items of a  tuple by use of indexes
print(counties[3])

